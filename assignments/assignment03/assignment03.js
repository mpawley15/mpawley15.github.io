// create an "add1" function
function add1 (elem) {
  if(elem.children[2].innerHTML == "-") {
    elem.children[2].innerHTML = "1";
	
  }else {
    let currentScore = elem.children[2].innerHTML;
    currentScore = Number.parseInt(currentScore);
    elem.children[2].innerHTML = currentScore + 1;
	
  }
}
function remove1(elem){
    let currentScore =elem.children[2].innerHTML;
    currentScore = Number.parseInt(currentScore);
	if(currentScore - 1 <=0){
		elem.children[2].innerHTML = "-";
	}else{
		elem.children[2].innerHTML = currentScore - 1;
		
	}
  
}
//function for overScore
function score(elem){
	let currentScore =elem.children[2].innerHTML;
	const par = 4;
	if(currentScore > 4){
		let overScore = currentScore - par;
		elem.children[3].innerHTML = overScore;
	}else{
		elem.children[3].innerHTML = "-";
	}
}
function clear(elem){
	elem.children[2].innerHTML = "-";
	elem.children[3].innerHTML = "-";
}

function addTotalScore(){
	let tPar = 0
	let tScore = 0
	let tOver = 0
	for(let i = 1; i<=18; i++){
		if (elem[i].children[1].innerHTML != "-"){
            tPar += Number.parseInt(elem[i].children[1].innerHTML);
		}
        if (elem[i].children[2].innerHTML != "-"){
            tScore += Number.parseInt(elem[i].children[2].innerHTML);
		}
        if (elem[i].children[3].innerHTML != "-"){
            tOver += Number.parseInt(elem[i].children[3].innerHTML);
		}
	}
		elem[19].children[1].innerHTML = tPar;
		elem[19].children[2].innerHTML = tScore;
		elem[19].children[3].innerHTML = tOver;	
}
